package ch02_operator;

public class Ex02_Operator {

	public static void main(String[] args) {
     //산술연산자
	// num1,num2 두개의 정수형 변수를 선언하고 값은 마음대로
	// 두 변수의 +,-,*,/,% 연산결과를 print()를 활용하여 출력
	
	 int num1 = 15;
	 int num2 = 13;
	 
	 System.out.println(num1 + num2);
	 System.out.println(num1 - num2);
	 System.out.println(num1 * num2);
	 System.out.println(num1 / num2);
	 System.out.println(num1 % num2);
	 
	 
	 

	}

}
